    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        /**
         * Takes text values from the frames fields and assigns them to a 
         * new customer object.  Also verifies that the phone number is 
         * the correct length.
         */
        String lastName = tfLastName.getText();
        newCust.setLastName(lastName);
        String firstName = tfFirstName.getText();
        newCust.setFirstName(firstName);
        String birthday = tfBirthdate.getText();
        newCust.setDOB(birthday);
        String pNumber = tfPhoneNumber.getText();
        String password = tfPassword.getText();
        newCust.setPassword(password);
        long phoneNumber = Long.parseLong(pNumber);
        try{
            if(phoneNumber >= 1000000000L && phoneNumber <= 9999999999L){
                newCust.setMemberID(phoneNumber);
                member.addMembers(newCust);
                JOptionPane.showMessageDialog(this, "Registration Successful!");
                this.setVisible(false);
            }else{
                JOptionPane.showMessageDialog(this, "Incorrect number of phone number digits. Please try again.");
            }
            }catch(InputMismatchException e){
        JOptionPane.showMessageDialog(this, "Invalid input.");
        }
    }//GEN-LAST:event_btnRegisterActionPerformed

