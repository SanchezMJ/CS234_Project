    private void ConfirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ConfirmActionPerformed
        /**
         * Assigns text field values to a new GiftCard Object and charges that 
         * object the total amount.  then checks to see if the current customer 
         * is a guest and if not, adds points before printing a receipt.
         */
        double total = cart.getTotalWithTax();
        String ccnum = tfCardNumber.getText();
        String expDate = tfExpDate.getText();
        Payment giftCard = new GiftCardPayment(total, ccnum, expDate);
        giftCard.addPayment(giftCard);
        if (customer.getFirstName().equals("Guest")) {
            cart.setAmountPaid(totalWithTax);
                new ReceiptGUI(customer, cart, change, totalWithTax).setVisible(true);

            } else {
                double points = total/1;
                customer.addPoints(points);
                cart.setAmountPaid(totalWithTax);
                new ReceiptGUI(customer, cart, change, totalWithTax).setVisible(true);
            }
        //System.out.println("complete, cart emptied, amountPaid ==0");   
        cart.emptyCart();
        //cart.setAmountPaid(0.0);
        double ap = cart.getAmountPaid();
        System.out.println("AmountPaid: " + ap);
        this.setVisible(false);
    }//GEN-LAST:event_ConfirmActionPerformed

